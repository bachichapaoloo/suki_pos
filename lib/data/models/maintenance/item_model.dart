import '../../../domain/entities/maintenance/item.dart';
import 'item_price_model.dart';

class ItemModel extends Item {
  const ItemModel({
    super.id,
    required super.itemCode,
    super.barcode,
    required super.name,
    required super.printName,
    super.labelName,
    super.itemDetails,
    super.isLabelSameAsReceipt,
    required super.categoryId,
    required super.departmentId,
    super.unitId,
    required super.costPrice,
    super.markupPercentage,
    super.conversionQty,
    super.minStockLevel,
    super.maxStockLevel,
    super.assignedPrinter,
    super.displayImage,
    super.buttonIndex,
    super.isDiscountExempt,
    super.isVatExempt,
    super.isCombo,
    super.isFinishedGood,
    super.isComposition,
    super.isRawMaterial,
    super.isGiftCheck,
    super.giftCheckId,
    super.discCapAmount,
    super.discCapPercentage,
    super.isActive,
    super.prices,
  });

  factory ItemModel.fromRelationalMaps({
    required Map<String, dynamic> itemMap,
    required List<Map<String, dynamic>> priceMaps,
  }) {
    return ItemModel(
      id: itemMap['id'] as int?,
      itemCode: itemMap['item_code'] as String,
      barcode: itemMap['barcode'] as String?,
      name: itemMap['name'] as String,
      printName: itemMap['print_name'] as String,
      labelName: itemMap['label_name'] as String?,
      itemDetails: itemMap['item_details'] as String?,
      isLabelSameAsReceipt: (itemMap['is_label_same_as_receipt'] as int? ?? 1) == 1,
      categoryId: itemMap['category_id'] as int,
      departmentId: itemMap['department_id'] as int,
      unitId: itemMap['unit_id'] as int?,
      costPrice: (itemMap['cost_price'] as num).toDouble(),
      markupPercentage: (itemMap['markup_percentage'] as num?)?.toDouble(),
      conversionQty: (itemMap['conversion_qty'] as num? ?? 1.0).toDouble(),
      minStockLevel: (itemMap['min_stock_level'] as num? ?? 0.0).toDouble(),
      maxStockLevel: (itemMap['max_stock_level'] as num? ?? 0.0).toDouble(),
      assignedPrinter: itemMap['assigned_printer'] as String?,
      displayImage: itemMap['display_image'] as String?,
      buttonIndex: itemMap['button_index'] as int?,
      isDiscountExempt: (itemMap['is_discount_exempt'] as int? ?? 0) == 1,
      isVatExempt: (itemMap['is_vat_exempt'] as int? ?? 0) == 1,
      isCombo: (itemMap['is_combo'] as int? ?? 0) == 1,
      isFinishedGood: (itemMap['is_finished_good'] as int? ?? 0) == 1,
      isComposition: (itemMap['is_composition'] as int? ?? 0) == 1,
      isRawMaterial: (itemMap['is_raw_material'] as int? ?? 0) == 1,
      isGiftCheck: (itemMap['is_gift_check'] as int? ?? 0) == 1,
      giftCheckId: itemMap['gift_check_id'] as int?,
      discCapAmount: (itemMap['disc_cap_amount'] as num? ?? 0.0).toDouble(),
      discCapPercentage: (itemMap['disc_cap_percentage'] as num? ?? 0.0).toDouble(),
      isActive: (itemMap['is_active'] as int? ?? 1) == 1,
      prices: priceMaps.map((m) => ItemPriceModel.fromMap(m)).toList(),
    );
  }

  Map<String, dynamic> toMasterTableMap() {
    return {
      'item_code': itemCode,
      'barcode': barcode,
      'name': name,
      'print_name': printName,
      'label_name': labelName,
      'item_details': itemDetails,
      'is_label_same_as_receipt': isLabelSameAsReceipt ? 1 : 0,
      'category_id': categoryId,
      'department_id': departmentId,
      'unit_id': unitId,
      'cost_price': costPrice,
      'markup_percentage': markupPercentage,
      'conversion_qty': conversionQty,
      'min_stock_level': minStockLevel,
      'max_stock_level': maxStockLevel,
      'assigned_printer': assignedPrinter,
      'display_image': displayImage,
      'button_index': buttonIndex,
      'is_discount_exempt': isDiscountExempt ? 1 : 0,
      'is_vat_exempt': isVatExempt ? 1 : 0,
      'is_combo': isCombo ? 1 : 0,
      'is_finished_good': isFinishedGood ? 1 : 0,
      'is_composition': isComposition ? 1 : 0,
      'is_raw_material': isRawMaterial ? 1 : 0,
      'is_gift_check': isGiftCheck ? 1 : 0,
      'gift_check_id': giftCheckId,
      'disc_cap_amount': discCapAmount,
      'disc_cap_percentage': discCapPercentage,
      'is_active': isActive ? 1 : 0,
      'updated_at': DateTime.now().toIso8601String(),
    };
  }
}
