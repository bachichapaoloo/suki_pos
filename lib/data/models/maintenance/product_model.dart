import 'package:suki_pos/domain/entities/maintenance/product.dart';

class ProductModel extends Product {
  const ProductModel({
    required super.id,
    required super.itemCode,
    required super.name,
    required super.printName,
    required super.categoryId,
    required super.departmentId,
    super.barcode,
    super.labelName,
    super.itemDetails,
    super.isLabelSameAsReceipt,
    super.unitId,
    super.costPrice,
    super.markupPercentage,
    super.conversionQty,
    super.assignedPrinter,
    super.displayImage,
    super.buttonIndex,
    super.minStockLevel,
    super.maxStockLevel,
    super.isDiscountExempt,
    super.isVatExempt,
    super.isCombo,
    super.isFinishedGood,
    super.isComposition,
    super.isRawMaterial,
    super.isGiftCheck,
    super.giftCheckId,
    super.discCapAmount,
    super.discCapPercentage,
    super.isActive,
    super.createdAt,
    super.updatedAt,
  });

  factory ProductModel.fromMap(Map<String, dynamic> map) {
    return ProductModel(
      id: map['id'] as int,
      barcode: map['barcode'] as String?,
      itemCode: map['item_code'] as String,
      name: map['name'] as String,
      printName: map['print_name'] as String,
      labelName: map['label_name'] as String?,
      itemDetails: map['item_details'] as String?,
      isLabelSameAsReceipt: (map['is_label_same_as_receipt'] as int) == 1,
      categoryId: map['category_id'] as int,
      departmentId: map['department_id'] as int,
      unitId: map['unit_id'] as int?,
      costPrice: (map['cost_price'] as num).toDouble(),
      markupPercentage: (map['markup_percentage'] as num?)?.toDouble(),
      conversionQty: (map['conversion_qty'] as num).toDouble(),
      assignedPrinter: map['assigned_printer'] as String?,
      displayImage: map['display_image'] as String?,
      buttonIndex: map['button_index'] as int?,
      minStockLevel: (map['min_stock_level'] as num).toDouble(),
      maxStockLevel: (map['max_stock_level'] as num).toDouble(),
      isDiscountExempt: (map['is_discount_exempt'] as int) == 1,
      isVatExempt: (map['is_vat_exempt'] as int) == 1,
      isCombo: (map['is_combo'] as int) == 1,
      isFinishedGood: (map['is_finished_good'] as int) == 1,
      isComposition: (map['is_composition'] as int) == 1,
      isRawMaterial: (map['is_raw_material'] as int) == 1,
      isGiftCheck: (map['is_gift_check'] as int) == 1,
      giftCheckId: map['gift_check_id'] as int?,
      discCapAmount: (map['disc_cap_amount'] as num).toDouble(),
      discCapPercentage: (map['disc_cap_percentage'] as num).toDouble(),
      isActive: (map['is_active'] as int) == 1,
      createdAt: map['created_at'] != null
          ? DateTime.parse(map['created_at'] as String)
          : null,
      updatedAt: map['updated_at'] != null
          ? DateTime.parse(map['updated_at'] as String)
          : null,
    );
  }

  factory ProductModel.fromEntity(Product entity) {
    return ProductModel(
      id: entity.id,
      barcode: entity.barcode,
      itemCode: entity.itemCode,
      name: entity.name,
      printName: entity.printName,
      labelName: entity.labelName,
      itemDetails: entity.itemDetails,
      isLabelSameAsReceipt: entity.isLabelSameAsReceipt,
      categoryId: entity.categoryId,
      departmentId: entity.departmentId,
      unitId: entity.unitId,
      costPrice: entity.costPrice,
      markupPercentage: entity.markupPercentage,
      conversionQty: entity.conversionQty,
      assignedPrinter: entity.assignedPrinter,
      displayImage: entity.displayImage,
      buttonIndex: entity.buttonIndex,
      minStockLevel: entity.minStockLevel,
      maxStockLevel: entity.maxStockLevel,
      isDiscountExempt: entity.isDiscountExempt,
      isVatExempt: entity.isVatExempt,
      isCombo: entity.isCombo,
      isFinishedGood: entity.isFinishedGood,
      isComposition: entity.isComposition,
      isRawMaterial: entity.isRawMaterial,
      isGiftCheck: entity.isGiftCheck,
      giftCheckId: entity.giftCheckId,
      discCapAmount: entity.discCapAmount,
      discCapPercentage: entity.discCapPercentage,
      isActive: entity.isActive,
      createdAt: entity.createdAt,
      updatedAt: entity.updatedAt,
    );
  }

  Map<String, dynamic> toMap() {
    return {
      if (id != 0) 'id': id,
      'barcode': barcode,
      'item_code': itemCode,
      'name': name,
      'print_name': printName,
      'label_name': labelName,
      'item_details': itemDetails,
      'is_label_same_as_receipt': isLabelSameAsReceipt ? 1 : 0,
      'category_id': categoryId,
      'department_id': departmentId,
      'unit_id': unitId,
      'cost_price': costPrice,
      'markup_percentage': markupPercentage,
      'conversion_qty': conversionQty,
      'assigned_printer': assignedPrinter,
      'display_image': displayImage,
      'button_index': buttonIndex,
      'min_stock_level': minStockLevel,
      'max_stock_level': maxStockLevel,
      'is_discount_exempt': isDiscountExempt ? 1 : 0,
      'is_vat_exempt': isVatExempt ? 1 : 0,
      'is_combo': isCombo ? 1 : 0,
      'is_finished_good': isFinishedGood ? 1 : 0,
      'is_composition': isComposition ? 1 : 0,
      'is_raw_material': isRawMaterial ? 1 : 0,
      'is_gift_check': isGiftCheck ? 1 : 0,
      'gift_check_id': giftCheckId,
      'disc_cap_amount': discCapAmount,
      'disc_cap_percentage': discCapPercentage,
      'is_active': isActive ? 1 : 0,
      if (createdAt != null) 'created_at': createdAt!.toIso8601String(),
      if (updatedAt != null) 'updated_at': updatedAt!.toIso8601String(),
    };
  }
}
